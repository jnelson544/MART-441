class Super {
    constructor(name, secretIdentity, powers, outfit, color, size, x, y, w, h) {
    this.name = name;
    this.secretIdentity = secretIdentity;
    this.powers = powers;
    this.outfit = outfit;
    this.color = color;
    this.size = size;
    this.x = x;
    this.y = y;
    this.w = w;
    this.h = h;
    }

    setName(name)
    {
        this.name = name;
    }
    get Name()
    {
        return this.name;
    }

    setSecret(secretIdentity)
    {
        this.secretIdentity = secretIdentity;
    }
    get Secret()
    {
        return this.secretIdentity;
    }

    setPowers(powers)
    {
        this.powers = powers;
    }
    get Powers()
    {
        return this.powers
    }

    setOutfit(outfit)
    {
        this.outfit = outfit;
    }
    get Outfit()
    {
        return this.outfit;
    }

    setColor(color)
    {
        this.color = color;
    }
    get Color()
    {
        return this.color;
    }

    setSize(size)
    {
        this.size = size;
    }
    get Size()
    {
        return this.size;
    }
    setX(x)
    {
        this.x = x;
    }
    get X()
    {
        return this.x;
    }

    setY(y)
    {
        this.y = y;
    }

    get Y()
    {
        return this.y;
    }
    
    setWidth(w) 
    {
        this.w = w;
    }

    get Width()
    {
        return this.w;
    }

    setHeight(h) 
    {
        this.h = h;
    }

    get Height()
    {
        return this.h;
    }

}


